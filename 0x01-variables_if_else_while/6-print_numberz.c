#include <stdlib.h>
#include <stdio.h>
#include <time.h>
/**
 * main - print last digit in random number
 *
 * Description: print last digit in random number
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	int n = 10, i;

	for (i = 0; i < n; i++)
	{
		putchar(i);
	}
	putchar('\n');

	return (0);
}
